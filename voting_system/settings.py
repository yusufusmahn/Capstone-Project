    import django
    django.setup()
    from django.contrib.auth import get_user_model
    from voting.models import VoterProfile  # import where your profile is

    User = get_user_model()
    admin_phone = os.getenv('ADMIN_PHONE')
    admin_password = os.getenv('ADMIN_PASSWORD')

    if not admin_phone or not admin_password:
        print("‚ö†Ô∏è ADMIN_PHONE or ADMIN_PASSWORD not set ‚Äî skipping superuser creation.")
    else:
        if not User.objects.filter(phone_number=admin_phone).exists():
            admin_user = User.objects.create_superuser(
                name='Admin User',
                phone_number=admin_phone,
                password=admin_password,
            )
            print(f"‚úÖ Superuser created: {admin_phone}")

            # Optional: create voter profile for consistency
            VoterProfile.objects.get_or_create(
                user=admin_user,
                defaults={
                    'voter_id': 'ADMIN0001',
                    'registration_verified': True,
                    'can_vote': False,
                },
            )
            print("üßæ Linked VoterProfile created for superuser.")
        else:
            print(f"‚ÑπÔ∏è Superuser already exists: {admin_phone}")

